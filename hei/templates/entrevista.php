<br>
<p style="font-color: rgb(27, 61, 92); font-weight:bold">Olá Tita nasceste com uma deficiência que se chama “Ausência Congénita de Membros” certo?</p>
- “Sim, exatamente.”
<br><br/>
<p style="font-weight:bold">Em que consiste essa anomalia? Ocorreu antes do teu nascimento e por isso nasceste com ausência de que membros?</p>
- “Sim, foi antes do meu nascimento. Eu nasci sem duas mãos, metade de uma perna e sem os dois pés, não tendo nenhum membro completo.”
<br><br/>
<p style="font-weight:bold">Utilizas próteses apenas nas pernas, com que idade colocaste as próteses pela primeira vez?</p>
- “Sim, uso apenas nas pernas e uso desde um ano.”
<br><br/>
<p style="font-weight:bold">Sentiste muita dificuldade na adaptação?</p>
- “Ao início é sempre difícil de me habituar, mas depois sempre me consegui adaptar-me”
<br><br/>
<p style="font-weight:bold">À medida que foste crescendo, as próteses tiveram de ser trocadas. A cada troca sentes essa dificuldade?</p>
- “Sempre.”
<br><br/>
<p style="font-weight:bold">De que forma teres nascido assim, Tita, afetou o teu relacionamento com outras crianças?</p>
- “Isso nunca me afetou de forma alguma. Sempre me integrei nas escolas e nas turmas, tratavam-me muito bem, no início era estranho para as outras crianças, porque nunca tinham visto algo assim, ou não estavam habituadas. Mas acabavam por se habituar e esqueciam que eu era diferente delas.”
<br><br/>
<p style="font-weight:bold">Então nunca te sentiste discriminada pelas outras crianças, ou por adultos?</p>
- “Não.”
<br><br/>
<p style="font-weight:bold">A tua inclusão na escola foi complicada de alguma forma Tita, devido ao teu problema?</p>
- “Não, fui sempre bem recebida, mas ao início a minha mãe teve alguma dificuldade em colocar-me numa escola, por eu ter esta condição, pois as pessoas pensavam que eu não ia conseguir fazer as coisas, ou ensinarem-me. Mas acabou por conseguir e tempo depois correu tudo bem, pois ficaram a conhecer-me melhor e viram realmente as minhas capacidades.”
<br><br/>
<p style="font-weight:bold">Então foste recusada numa escola devido a teres uma deficiência?</p>
- “Sim.”
<br><br/>
<p style="font-weight:bold">Participas-te em todas as atividades desde que entraste na escola?</p>
- “Pelo que eu e a minha mãe nos lembramos, sim sempre conseguia fazer tudo. Mesmo que eu não conseguisse fazê-las, acabavam por adaptar a atividade para que eu pudesse participar.”
<br><br/>
<p style="font-weight:bold">E nos dias de hoje, sentes-te excluída de alguma forma?</p>
- “Não, não me sinto excluída de forma nenhuma, e acho que nunca senti. Claro que às vezes nem tudo é igual para mim, mas sempre houveram adaptações. As pessoas por vezes olham para mim e pensam “Á coitadinha ela não consegue fazer”. Mas eu consigo fazer tudo, e depois de me conhecerem realmente acabam por ver que não há razões para terem “pena”.”
