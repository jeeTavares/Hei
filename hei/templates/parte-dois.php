<p style="font-weight:bold">Nos dias de hoje, sentes alguma diferença seja a que nível for, entre a tua vida e a vida de outro adolescente?</p>
- “Não, porque eu faço tudo o que os outros adolescentes fazem, ou pelo menos tento. Às vezes até sou mais ativa e tento não deixar de o ser. Porque quando me deparo com algo novo em vez de dizer “Eu não consigo”, eu tento primeiro.”
<br><br/>
<p style="font-weight:bold">Sempre foste uma pessoa autónoma?</p>
- “Sim, os meus pais sempre me incentivaram e apoiaram, nunca fizeram as coisas por mim diziam-me sempre “Faz que tu consegues” e eu acabava sempre por fazer e conseguir.”
<br><br/>
<p style="font-weight:bold">Consideras que isso foi importante?</p>
-“Claro que sim. Tendo esse habito desde pequena, futuramente não irei precisar de ninguém para viver sozinha.”
<br><br/>
<p style="font-weight:bold">Pedes muita ajuda?</p>
-“Não, tento sempre primeiro fazer o que pretendo, só depois se vir que não consigo mesmo é que peço ajuda.”
<br><br/>
<p style="font-weight:bold">Consideras que a tua deficiência te vai prejudicar, futuramente, no mercado de trabalho?</p>
-“Eu acho que não, até porque já tive a experiência de estagiar numa instituição, onde fui bastante bem recebida. E também porque provei à sociedade que sou bastante desenrascada e sou capaz de fazer.”
<br><br/>
<p style="font-weight:bold">Na tua opinião qualquer criança que tenha qualquer tipo de deficiência, deve ser incentivada a participar na sociedade?</p>
- “Claro que sim, tal como eu presenciei esses momentos de inclusão, uma criança deficiente nunca deve ser excluída por pensarem que ela não consegue. A própria criança é que deve ter a oportunidade de mostrar que consegue e não excluírem a criança apenas pela especulação errada das outras pessoas.”
<br><br/>
<p style="font-weight:bold">Tita, tu com vinte anos de vida, o que dirias a um adulto que lida com crianças com deficiência?</p>
- “Não julguem que crianças deficientes não conseguem fazer as coisas, porque elas conseguem, cada uma à sua maneira e com o seu tempo.”
<br><br/>
<p style="font-weight:bold">E o que dirias a uma criança?</p>
- “Para não desistir, e para quando dissessem que ela não conseguia, para não acreditar nisso e tentar sempre.”
